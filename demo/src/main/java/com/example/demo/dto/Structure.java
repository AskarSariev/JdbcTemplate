package com.example.demo.dto;

public class Structure {
    private String idDoc;
    private String kodStruct;
    private String structureStr;

    public String getIdDoc() {
        return idDoc;
    }

    public void setIdDoc(String idDoc) {
        this.idDoc = idDoc;
    }

    public String getKodStruct() {
        return kodStruct;
    }

    public void setKodStruct(String kodStruct) {
        this.kodStruct = kodStruct;
    }

    public String getStructureStr() {
        return structureStr;
    }

    public void setStructureStr(String structureStr) {
        this.structureStr = structureStr;
    }
}
